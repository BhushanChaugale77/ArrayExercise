package com.prowings.forloop;

public class DoWhileDemo {

	public static void main(String[] args) {

		int i = 1;

		do {
			System.out.println("This is itration " + i);

			i++;
		} while (i <= 5);

		System.out.println(">>>>>>>>>>>>>>>>>>>>>");

		int j = 1;

		while (j <= 5) {
			System.out.println("This is iteration " + j);
			j++;
		}

	}

}
